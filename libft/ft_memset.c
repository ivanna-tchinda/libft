#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*s2;
	size_t	i;

	s2 = (char *)s;
	i = 0;
	while (i < n)
	{
		s2[i] = c;
		i++;
	}
	return (s);
}

#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*s2;

	s2 = (char *)s;
	while (*s2 != (char)c)
	{
		if (!*s2)
			return (0);
		s2++;
	}
	return (s2);
}
